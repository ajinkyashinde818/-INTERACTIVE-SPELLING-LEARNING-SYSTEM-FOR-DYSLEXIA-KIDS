    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText().trim();
        String password = jPasswordField1.getText().trim();
        String enteredOtp = jTextField2.getText().trim();

        if(username.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username cannot be empty");
        } else if(password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password cannot be empty");
        } else if(enteredOtp.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter the OTP");
        } else {
            System.out.println("Username: " + username);
            System.out.println("Password: " + password);
            System.out.println("Entered OTP: " + enteredOtp);

            if(new TeacherDBOP().isTeacherExisted(username, password)) {
                // Verify OTP
                if(enteredOtp.equals(generatedOTP)) {
                    // Set the static username for use in other frames
                    LoginFrame.username = jTextField1.getText().trim();
                    
                    OperationFrame of = new OperationFrame();
                    of.setVisible(true);
                    Dimension d = Toolkit.getDefaultToolkit().getScreenSize();
                    of.setSize(d);
                    this.dispose(); // Close login frame
                } else {
                    JOptionPane.showMessageDialog(null, "Incorrect OTP! Please check your OTP and try again.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username and Password are incorrect");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

