    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText().trim();
        String password = jPasswordField1.getText().trim();

        if(username.isEmpty()){
            JOptionPane.showMessageDialog(null, "Username cannot be empty");
        } else if(password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Password cannot be empty");
        } else {
            System.out.println("Username: " + username);
            System.out.println("Password: " + password);

            if(new TeacherDBOP().isTeacherExisted(username, password)) {
                // Get email address for the user
                String email = new EmailFetcher().getEmailAddress(username);
                
                
                    // Generate OTP
                    String otp = OTPGenerator.generateNumericOTP(4);
                    System.out.println("Generated OTP: " + otp);

                    // Send OTP via email
                    try {
                        String emailBody = "Dear Teacher,\n\n"
                            + "Your OTP for Spelling Learning System login is: " + otp + "\n\n"
                            + "This OTP is valid for a limited time. Please do not share this OTP with anyone.\n\n"
                            + "If you did not request this OTP, please ignore this email.\n\n"
                            + "Best regards,\nSpelling Learning System Team";
                        
                        new SendEmail().sendMailNow(emailBody, "Spelling Learning System - Login OTP", email);
                        JOptionPane.showMessageDialog(null, "OTP has been sent to your email address!");
                        generatedOTP = otp;
                        
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Failed to send OTP. Please try again.");
                        System.out.println("Error sending OTP: " + e.getMessage());
                    }
                
            } else {
                JOptionPane.showMessageDialog(null, "Username and Password are incorrect");
            }  
        }
    }//GEN-LAST:event_jButton4ActionPerformed

